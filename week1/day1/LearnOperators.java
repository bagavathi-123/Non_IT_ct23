package week1.day1;

public class LearnOperators {

	public static void main(String[] args) {
		
		int a=10;
		int b=6;
		
		// Arithmetic operators
		//add '+'
		System.out.println(a+b);
		//sub '-'
		System.out.println(a-b);
		//mul '*'
		System.out.println(a*b);
		//rem '%'
		System.out.println(a%b);
		//div '/'
		System.out.println(a/b);
		
		//Realtional operator or(comparison)
		//a is great than b '>'
		System.out.println(a>b);
		//a is less than b '<'
		System.out.println(a<b);
		//a is equal b '=='
		System.out.println(a==b);
		//a is not equal b '!='
		System.out.println(a!=b);

//Assignment operator
		int c=5;
		
		//logical operator
		//&&,||,!
		






	}

}
